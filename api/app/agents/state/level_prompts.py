LEVEL_ADJUSTMENT_RULES = {
    "upgrade": {
        "criteria": {
            "min_success_rate": 0.85,
            "min_questions": 5,
            "consecutive_correct": 3
        },
        "message": "表现优秀！已升级到下一难度等级。"
    },
    "downgrade": {
        "criteria": {
            "max_success_rate": 0.5,
            "min_questions": 3,
            "consecutive_wrong": 2
        },
        "message": "让我们降低难度，巩固基础。"
    }
}


